<?php
require_once('../Config/cls_conexion.model.php');
class Clase_Publicaciones
{
    public function todos()
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "SELECT Publicaciones.*, Publicaciones.Contenido as Contenido, Publicaciones.Fecha_Publicacion as Fecha_Publicacion, Usuarios.Nombre as NombreUsuario FROM `Publicaciones` INNER JOIN Usuarios ON Publicaciones.ID_usuario = Usuarios.ID_usuario";

            $result = mysqli_query($con, $cadena);
            return $result;
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
    public function uno($publicacionID)
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "SELECT * FROM `Publicaciones` WHERE ID_publicacion=$publicacionID";
            $result = mysqli_query($con, $cadena);
            return $result;
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
    public function insertar($ID_usuario, $Contenido)
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "INSERT INTO `Publicaciones`( `ID_usuario`, `Contenido`, `Fecha_publicacion`) VALUES ('$ID_usuario','$Contenido',CURDATE())";
            $result = mysqli_query($con, $cadena);
            return 'ok';
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
    public function actualizar($publicacionID, $ID_usuario, $Contenido)
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "UPDATE `Publicaciones` SET `ID_usuario`='$ID_usuario',`Contenido`='$Contenido' WHERE `ID_publicacion`='$publicacionID'";
            $result = mysqli_query($con, $cadena);
            return "ok";
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
    public function eliminar($PublicacionID)
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "delete from Publicaciones where ID_publicacion=$PublicacionID";
            $result = mysqli_query($con, $cadena);
            return "ok";
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
}
